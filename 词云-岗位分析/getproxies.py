def get_header():
    return {
        'User-Agent': random.choice(USER_AGENTS),
        'Accept': 'text/html,application/xhtml+xml,application/xml;q=0.9,*/*;q=0.8',
        'Accept-Language': 'en-US,en;q=0.5',
        'Connection': 'keep-alive',
        # 'Accept-Encoding': 'gzip, deflate',
    }


def proxyTest(pools):
    poolssuccess = []
    n = 1
    for ip, port in pools:
        print('测试第[%d]个ip 共[%d]个 可用%d个' % (n, len(pools), len(poolssuccess)))
        print()
        address = ip + ':' + port
        header = get_header()
        url = 'http://2018.ip138.com/ic.asp'
        try:
            response = requests.get(url, headers=header, proxies={"http": address}, timeout=1).text
            # print(response)
            if ip in response:
                print('可用ip:%s' % address)
                poolssuccess.append(address)
        except:
            pass
            # print('代理ip响应超时')

        n += 1
    return (list(set(poolssuccess)))


def write_proxy(succ_ips):
    if len(succ_ips) == 0:
        print('无可用数据')
        return
    else:
        # 写入本地
        f = open('./proxies.txt', 'w+')
        for address in succ_ips:
            f.write(address + '\n')
        f.close()

        print('写入完毕', time.strftime('%Y-%m-%d %H:%M:%S', time.localtime(time.time())))
        print(succ_ips)
        print()


def parse_ip(url='', reg=None):
    if url:
        if reg:
            heads = get_header()
            ret = requests.get(url, headers=heads).text
            pools = re.findall(reg, ret)
            print('已获取{}个'.format(len(pools)))
            print(pools)
            return pools
        else:
            raise TypeError('解析类型错误,只能用正则')
    else:
        raise ValueError('url出错,不能为空')


